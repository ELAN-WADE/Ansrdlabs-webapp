"use client"

import { useEffect } from "react"
import { Button } from "@/components/ui/button"

export default function Error({
  error,
  reset,
}: {
  error: Error & { digest?: string }
  reset: () => void
}) {
  useEffect(() => {
    console.error(error)
  }, [error])

  return (
    <div className="flex min-h-screen flex-col items-center justify-center px-4">
      <div className="mx-auto max-w-md text-center">
        <h2 className="mb-4 font-serif text-3xl font-bold text-foreground">Something went wrong!</h2>
        <p className="mb-8 text-muted-foreground">We encountered an error while loading this page. Please try again.</p>
        <Button onClick={() => reset()} size="lg">
          Try again
        </Button>
      </div>
    </div>
  )
}
